package classi.cartmanagement;

/**
 * Questa classe astratta fornisce un metodo astratto per stampare il carrello.
 */
public abstract class Astract {
    /**
     * Stampa il contenuto del carrello.
     *
     * @return {@code true} se la stampa ha avuto successo, altrimenti {@code false}.
     */
    public abstract boolean printCart();
}
